function GrowWareIdDef:getExpGrowId(gameId)
    local id = 0
    if gameId == JJGameDefine.GAME_ID_LORD then
        id = GrowWareIdDef.LORD_GROW_EXPERIENCE_ID
    elseif gameId == JJGameDefine.GAME_ID_LORD_LZ then
        id = GrowWareIdDef.LORDLZ_GROW_EXPERIENCE_ID
    elseif gameId == JJGameDefine.GAME_ID_LORD_HL then
        id = GrowWareIdDef.LORDHL_GROW_EXPERIENCE_ID
    elseif gameId == JJGameDefine.GAME_ID_LORD_PK then
        id = GrowWareIdDef.PKLORD_GROW_EXPERIENCE_ID
    elseif gameId == JJGameDefine.GAME_ID_POKER then
        id = GrowWareIdDef.POKER_GROW_EXPERIENCE_ID
    elseif gameId == JJGameDefine.GAME_ID_MAHJONG or gameId == JJGameDefine.GAME_ID_MAHJONG_TP then
        id = GrowWareIdDef.MAHJONG_GROW_EXPERIENCE_ID
    elseif gameId == JJGameDefine.GAME_ID_THREE_CARD then
        id = GrowWareIdDef.THREECARD_GROW_EXPERIENCE_ID
    elseif gameId == JJGameDefine.GAME_ID_NIUNIU then
        id = GrowWareIdDef.NIUNIU_GROW_EXPERIENCE_ID
    elseif gameId == JJGameDefine.GAME_ID_RUNFAST then
        id = GrowWareIdDef.RUNFAST_GROW_EXPERIENCE_ID
    elseif gameId == JJGameDefine.GAME_ID_MAHJONG_BR then
        id = GrowWareIdDef.MAHJONGBR_GROW_EXPERIENCE_ID
    elseif gameId == JJGameDefine.GAME_ID_MAHJONG_SC then
        id = GrowWareIdDef.MAHJONGSC_GROW_EXPERIENCE_ID
    elseif gameId == JJGameDefine.GAME_ID_INTERIM then
        id = GrowWareIdDef.INTERIM_GROW_EXPERIENCE_ID
    elseif gameId == JJGameDefine.GAME_ID_MAHJONGTDH then
        id = GrowWareIdDef.MAHJONGTDH_GROW_EXPERIENCE_ID
    end
    return id
end

return GrowWareIdDef
